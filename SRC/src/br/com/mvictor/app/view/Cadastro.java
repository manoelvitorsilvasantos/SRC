            cl.setRg(txtRg.getText());
            cl.setDtNasc(txtDtNasc.getText());
            cl.setSexo(comboxSexo.getSelectedItem().toString());
            cl.setEstadoCiv(comboxEstadoCivil.getSelectedItem().toString());
            
            Endereco end = new Endereco();
            end.setRua(txtRua.getText());
            end.setBairro(txtBairro.getText());
            end.setComplemento(txtComplemento.getText());
            end.setCep(txtCep.getText());
            end.setNumero((int)spinerNumero.getValue());
            end.setCidade(txtCidade.getText());
            end.setEstado(comboxEstado.getSelectedItem().toString());
   
