    public boolean validaCampos()
    {
        
        String nome = null;
        
        Validacao val = Validacao.getInstance();
        
        if(val.isEmpty(this.txtNome.getText()) || val.isNull(this.txtNome.getText()))
        {
            JOptionPane.showMessageDialog(this, "Campo nome vázio, porfavor preencher!", "Aviso", JOptionPane.WARNING_MESSAGE);
            this.txtNome.setBorder(new LineBorder(Color.RED));
            return false;
        }
        else
        {
            nome = this.txtNome.getText();
            this.txtNome.setText(nome.toUpperCase());
            this.txtNome.setBorder(new LineBorder(Color.BLACK));
        }
        if(val.isEmpty(this.txtDtNasc, "  /  /    ") || val.isNull(this.txtDtNasc, null))
        {
            JOptionPane.showMessageDialog(this, "Campo Data de nascimento vázio, porfavor preencher!", "Aviso", JOptionPane.WARNING_MESSAGE);
            this.txtDtNasc.setBorder(new LineBorder(Color.RED));
            return false;
        }
        else
        {
            this.txtDtNasc.setBorder(new LineBorder(Color.BLACK));
        }
        
        if(val.isEmpty(this.txtCpf, "   .   .   -  ") || val.isNull(this.txtCpf, null))
        {
            JOptionPane.showMessageDialog(this, "Campo CPF vázio, porfavor preencher!", "Aviso", JOptionPane.WARNING_MESSAGE);
            this.txtCpf.setBorder(new LineBorder(Color.RED));
            return false;
        }
        else
        {
            this.txtCpf.setBorder(new LineBorder(Color.BLACK));
        }
        
        if(val.isEmpty(this.txtRg, "  .   .   - ") || val.isNull(this.txtRg, null))
        {
            JOptionPane.showMessageDialog(this, "Campo RG vázio, porfavor preencher!", "Aviso", JOptionPane.WARNING_MESSAGE);
            this.txtRg.setBorder(new LineBorder(Color.RED));
            return false;
        }
        else
        {
            this.txtRg.setBorder(new LineBorder(Color.BLACK));
        }
        
        if(val.isEmpty(this.txtCelP, "(  )-     -    ") || val.isNull(this.txtCelP, null))
        {
            JOptionPane.showMessageDialog(this, "Campo número de celular vázio, porfavor preencher!", "Aviso", JOptionPane.WARNING_MESSAGE);
            this.txtCelP.setBorder(new LineBorder(Color.RED));
            return false;
        }
        else
        {
            this.txtCelP.setBorder(new LineBorder(Color.BLACK));
        }
        
        if(val.isEmpty(txtCelS,"(  )-     -    ") || val.isNull(txtCelS, "(  )-     -    "))
        {
            this.txtCelS.setText(null);
        }
        
        if(val.isEmpty(this.txtEmail.getText()) || val.isNull(this.txtEmail.getText()))
        {
            JOptionPane.showMessageDialog(this, "Campo E-mail vázio, porfavor preencher!", "Aviso", JOptionPane.WARNING_MESSAGE);
            this.txtEmail.setBorder(new LineBorder(Color.RED));
            return false;
        }
        else
        {
            this.txtEmail.setBorder(new LineBorder(Color.BLACK));
        }
        
        if(val.isEmpty(this.txtRua.getText()) || val.isNull(this.txtRua.getText()))
        {
            JOptionPane.showMessageDialog(this, "Campo nome da rua está vázio, porfavor preencher!", "Aviso", JOptionPane.WARNING_MESSAGE);
            this.txtRua.setBorder(new LineBorder(Color.RED));
            return false;
        }
        else
        {
            this.txtRua.setBorder(new LineBorder(Color.BLACK));
        }
        
        if(val.isEmpty(this.txtBairro.getText()) || val.isNull(this.txtBairro.getText()))
        {
            JOptionPane.showMessageDialog(this, "Campo nome do bairro está vázio, porfavor preencher!", "Aviso", JOptionPane.WARNING_MESSAGE);
            this.txtBairro.setBorder(new LineBorder(Color.RED));
            return false;
        }
        else
        {
            this.txtBairro.setBorder(new LineBorder(Color.BLACK));
        }
        
        if(val.isEmpty(this.txtCidade.getText()) || val.isNull(this.txtCidade.getText()))
        {
            JOptionPane.showMessageDialog(this, "Campo nome da cidade está vázio, porfavor preencher!", "Aviso", JOptionPane.WARNING_MESSAGE);
            this.txtCidade.setBorder(new LineBorder(Color.RED));
            return false;
        }
        else
        {
            this.txtCidade.setBorder(new LineBorder(Color.BLACK));
        }
        
        if(val.isEmpty(this.txtComplemento.getText()) || val.isNull(this.txtComplemento.getText()))
        {
            JOptionPane.showMessageDialog(this, "Campo complemento vázio, porfavor preencher!", "Aviso", JOptionPane.WARNING_MESSAGE);
            this.txtComplemento.setBorder(new LineBorder(Color.RED));
            return false;
        }
        else
        {
            this.txtComplemento.setBorder(new LineBorder(Color.BLACK));
        }
        
        if(val.isEmpty(this.txtCep, "  .   -   ") || val.isNull(this.txtCep, null))
        {
            JOptionPane.showMessageDialog(this, "Campo número de cep está vázio, porfavor preencher!", "Aviso", JOptionPane.WARNING_MESSAGE);
            this.txtCep.setBorder(new LineBorder(Color.RED));
            return false;
        }
        else
        {
            this.txtCep.setBorder(new LineBorder(Color.BLACK));
        }
        
        if(val.isEmpty(this.spinerNumero.getValue().toString()) || val.isNull(this.spinerNumero.getValue().toString()))
        {
            JOptionPane.showMessageDialog(this, "Campo número da casa está vázio, porfavor preencher!", "Aviso", JOptionPane.WARNING_MESSAGE);
            this.spinerNumero.setBorder(new LineBorder(Color.RED));
            return false;
        }
        else
        {
            this.spinerNumero.setBorder(new LineBorder(Color.BLACK));
        }
        
        if(!val.isEmailValid(this.txtEmail.getText()))
        {
            JOptionPane.showMessageDialog(this, "O email digitado é inválido, porfavor digite corretamente!", "Aviso", JOptionPane.WARNING_MESSAGE);
            this.txtEmail.setBorder(new LineBorder(Color.RED));
            return false;
        }
        else
        {
            this.txtEmail.setBorder(new LineBorder(Color.BLACK));
        }
        
        
        return true;
    }
    
    
