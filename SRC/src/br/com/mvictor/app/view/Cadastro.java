            cont.setCelP(txtCelP.getText());
            cont.setCelS(txtCelS.getText());
            cont.setEmail(txtEmail.getText());
            cont.setPessoa(cl);
            
            List <Contato> listaTel = new ArrayList();
            listaTel.add(cont);
            
